	<h3 class="adsimple-112580075">Was ist ein Content Delivery Network?</h3>
	<p>
		Wir nutzen auf unserer Website ein sogenanntes Content Delivery Network. Meistens wird ein
		solchen Netzwerk nur CDN genannt. Ein CDN hilft uns, unsere Website, unabhängig von Ihrem
		Standort, schnell und problemlos zu laden. Dabei werden auch personenbezogene Daten von Ihnen
		auf den Servern des eingesetzten CDN-Anbieters gespeichert, verwaltet und verarbeitet.
		Nachfolgend gehen wir allgemein näher auf den Dienst und dessen Datenverarbeitung ein. Genaue
		Informationen über den Umgang mit Ihren Daten finden Sie in der jeweiligen Datenschutzerklärung
		des Anbieters.
	</p>
	<p>
		Jedes Content Delivery Network (CDN) ist ein Netzwerk regional verteilter Server, die alle über
		das Internet miteinander verbunden sind. Über dieses Netzwerk können Inhalte von Websites
		(speziell auch sehr große Dateien) auch bei großen Lastspitzen schnell und reibungslos
		ausgeliefert werden. Das CDN legt dafür eine Kopie unserer Website auf Ihren Servern an. Da
		diese Server weltweit verteilt sind, kann die Website schnell ausgeliefert werden. Die
		Datenübertragung zu Ihrem Browser wird folglich durch das CDN deutlich verkürzt.
	</p>
	<h3 class="adsimple-112580075">
		Warum verwenden wir ein Content Delivery Network für unsere Website?
	</h3>
	<p>
		Eine schnell ladende Website ist Teil unserer Dienstleistung. Wir wissen natürlich, wie nervig
		es ist, wenn eine Website im Schneckentempo lädt. Meist verliert man sogar die Geduld und sucht
		das Weite, bevor die Website vollständig geladen ist. Das wollen wir natürlich vermeiden. Daher
		gehört eine schnell ladende Website ganz selbstverständlich zu unserem Websiteangebot. Mit einem
		Content Delivery Network wird in Ihrem Browser unsere Website deutlich schneller geladen.
		Besonders hilfreich ist der Einsatz des CDNs wenn Sie im Ausland sind, weil die Website von
		einem Server in Ihrer Nähe ausgeliefert wird.
	</p>
	<h3 class="adsimple-112580075">Welche Daten werden verarbeitet?</h3>
	<p>
		Wenn Sie eine Website bzw. die Inhalte einer Website anfordern und diese in einem CDN
		zwischengespeichert sind, leitet das CDN die Anforderung an den von Ihnen am nächsten gelegenen
		Server und dieser liefert die Inhalte aus. Content Delivery Networks sind so aufgebaut, dass
		JavaScript-Bibliotheken heruntergeladen werden können und auf npm- und Github-Servern gehostet
		werden. Alternativ können bei den meisten CDNs auch WordPress-Plugins geladen werden, wenn diese
		auf <a class="adsimple-112580075" href="https://wordpress.org/" target="_blank" rel="noopener"
			>WordPress.org</a
		> gehostet werden. Ihr Browser kann personenbezogene Daten an das von uns eingesetzte Content Delivery
		Network senden. Dabei handelt es sich etwa um Daten wie IP-Adresse, Browsertyp, Browserversion, welche
		Webseite geladen wird oder Uhrzeit und Datum des Seitenbesuchs. Diese Daten werden vom CDN gesammelt
		und auch gespeichert. Ob zur Datenspeicherung Cookies verwendet werden, hängt von dem eingesetzten
		Network ab. Bitte lesen Sie sich dafür die Datenschutztexte des jeweiligen Dienstes durch.
	</p>
